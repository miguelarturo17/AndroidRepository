package sbs.milpo.model.bl.sinc;

import sbs.milpo.model.dal.Lectura;


public class SincronizacionLectura {

	private String id= "";
	private String codigo_sincronizacionLectura= "";
	private String codigo_usuario= "";
	private String estado= "";
	private String fecha= "";
	private Lectura[] lecturaList=null;
        
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the codigo_sincronizacionLectura
	 */
	public String getCodigo_sincronizacionLectura() {
		return codigo_sincronizacionLectura;
	}
	/**
	 * @param codigo_sincronizacionLectura the codigo_sincronizacionLectura to set
	 */
	public void setCodigo_sincronizacionLectura(String codigo_sincronizacionLectura) {
		this.codigo_sincronizacionLectura = codigo_sincronizacionLectura;
	}

	/**
	 * @return the estado
	 */
	public String getEstado() {
		return estado;
	}
	/**
	 * @param estado the estado to set
	 */
	public void setEstado(String estado) {
		this.estado = estado;
	}
	/**
	 * @return the fecha
	 */
	public String getFecha() {
		return fecha;
	}
	/**
	 * @param fecha the fecha to set
	 */
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	/**
	 * @return the codigo_usuario
	 */
	public String getCodigo_usuario() {
		return codigo_usuario;
	}
	/**
	 * @param codigo_usuario the codigo_usuario to set
	 */
	public void setCodigo_usuario(String codigo_usuario) {
		this.codigo_usuario = codigo_usuario;
	}
	/**
	 * @return the lecturaList
	 */
	public Lectura[] getLecturaList() {
		return lecturaList;
	}
	/**
	 * @param lecturaList the lecturaList to set
	 */
	public void setLecturaList(Lectura[] lecturaList) {
		this.lecturaList = lecturaList;
	}
	
	
	
	
    
}
