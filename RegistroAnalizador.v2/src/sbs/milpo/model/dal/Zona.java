package sbs.milpo.model.dal;


public class Zona {

	private String id = "";
	private String codigo_zona = "";
	private String descripcion = "";
    private String codigo_tipoZona = "";
    private String codigo_unidadMinera = "";
    private String codigo_empresa = "";
    private String tipoZona = "";
    private String estado = "";

    
    
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the codigo_zona
	 */
	public String getCodigo_zona() {
		return codigo_zona;
	}
	/**
	 * @param codigo_zona the codigo_zona to set
	 */
	public void setCodigo_zona(String codigo_zona) {
		this.codigo_zona = codigo_zona;
	}


	/**
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}
	/**
	 * @param descripcion the descripcion to set
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	/**
	 * @return the codigo_tipoZona
	 */
	public String getCodigo_tipoZona() {
		return codigo_tipoZona;
	}
	/**
	 * @param codigo_tipoZona the codigo_tipoZona to set
	 */
	public void setCodigo_tipoZona(String codigo_tipoZona) {
		this.codigo_tipoZona = codigo_tipoZona;
	}
	/**
	 * @return the codigo_unidadMinera
	 */
	public String getCodigo_unidadMinera() {
		return codigo_unidadMinera;
	}
	/**
	 * @param codigo_unidadMinera the codigo_unidadMinera to set
	 */
	public void setCodigo_unidadMinera(String codigo_unidadMinera) {
		this.codigo_unidadMinera = codigo_unidadMinera;
	}
	/**
	 * @return the codigo_empresa
	 */
	public String getCodigo_empresa() {
		return codigo_empresa;
	}
	/**
	 * @param codigo_empresa the codigo_empresa to set
	 */
	public void setCodigo_empresa(String codigo_empresa) {
		this.codigo_empresa = codigo_empresa;
	}
	/**
	 * @return the estado
	 */
	public String getEstado() {
		return estado;
	}
	/**
	 * @param estado the estado to set
	 */
	public void setEstado(String estado) {
		this.estado = estado;
	}
	/**
	 * @return the tipoZona
	 */
	public String getTipoZona() {
		return tipoZona;
	}
	/**
	 * @param tipoZona the tipoZona to set
	 */
	public void setTipoZona(String tipoZona) {
		this.tipoZona = tipoZona;
	}

    
    
}
