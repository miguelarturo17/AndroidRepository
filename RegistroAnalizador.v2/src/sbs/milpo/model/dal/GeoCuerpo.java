package sbs.milpo.model.dal;


public class GeoCuerpo {

	private String id = "";
	private String codigo_cuerpo = "";
	private String codigo_empresa = "";
    private String codigo_unidadminera = "";
    private String nro_cuerpo = "";
    private String estado = "";
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the codigo_cuerpo
	 */
	public String getCodigo_cuerpo() {
		return codigo_cuerpo;
	}
	/**
	 * @param codigo_cuerpo the codigo_cuerpo to set
	 */
	public void setCodigo_cuerpo(String codigo_cuerpo) {
		this.codigo_cuerpo = codigo_cuerpo;
	}
	/**
	 * @return the codigo_empresa
	 */
	public String getCodigo_empresa() {
		return codigo_empresa;
	}
	/**
	 * @param codigo_empresa the codigo_empresa to set
	 */
	public void setCodigo_empresa(String codigo_empresa) {
		this.codigo_empresa = codigo_empresa;
	}
	/**
	 * @return the codigo_unidadminera
	 */
	public String getCodigo_unidadminera() {
		return codigo_unidadminera;
	}
	/**
	 * @param codigo_unidadminera the codigo_unidadminera to set
	 */
	public void setCodigo_unidadminera(String codigo_unidadminera) {
		this.codigo_unidadminera = codigo_unidadminera;
	}
	/**
	 * @return the nro_cuerpo
	 */
	public String getNro_cuerpo() {
		return nro_cuerpo;
	}
	/**
	 * @param nro_cuerpo the nro_cuerpo to set
	 */
	public void setNro_cuerpo(String nro_cuerpo) {
		this.nro_cuerpo = nro_cuerpo;
	}
	/**
	 * @return the estado
	 */
	public String getEstado() {
		return estado;
	}
	/**
	 * @param estado the estado to set
	 */
	public void setEstado(String estado) {
		this.estado = estado;
	}

    
    
    
    
}
