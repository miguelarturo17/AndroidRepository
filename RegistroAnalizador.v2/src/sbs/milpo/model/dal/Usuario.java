package sbs.milpo.model.dal;

public class Usuario {

    private String id = "";
    private String codigo_usuario = "";
    private String codigo_trabajador = "";
    private String usuario = "";
    private String password = "";
    private String estado = "";
    private String sesion = "";
    private String nombre_trabajador = "";
    private String administrador="";
    
    
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the codigo_usuario
	 */
	public String getCodigo_usuario() {
		return codigo_usuario;
	}
	/**
	 * @param codigo_usuario the codigo_usuario to set
	 */
	public void setCodigo_usuario(String codigo_usuario) {
		this.codigo_usuario = codigo_usuario;
	}
	/**
	 * @return the codigo_trabajador
	 */
	public String getCodigo_trabajador() {
		return codigo_trabajador;
	}
	/**
	 * @param codigo_trabajador the codigo_trabajador to set
	 */
	public void setCodigo_trabajador(String codigo_trabajador) {
		this.codigo_trabajador = codigo_trabajador;
	}
	/**
	 * @return the usuario
	 */
	public String getUsuario() {
		return usuario;
	}
	/**
	 * @param usuario the usuario to set
	 */
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	/**
	 * @return the estado
	 */
	public String getEstado() {
		return estado;
	}
	/**
	 * @param estado the estado to set
	 */
	public void setEstado(String estado) {
		this.estado = estado;
	}
	/**
	 * @return the sesion
	 */
	public String getSesion() {
		return sesion;
	}
	/**
	 * @param sesion the sesion to set
	 */
	public void setSesion(String sesion) {
		this.sesion = sesion;
	}
	/**
	 * @return the nombre_trabajador
	 */
	public String getNombre_trabajador() {
		return nombre_trabajador;
	}
	/**
	 * @param nombre_trabajador the nombre_trabajador to set
	 */
	public void setNombre_trabajador(String nombre_trabajador) {
		this.nombre_trabajador = nombre_trabajador;
	}
	/**
	 * @return the administrador
	 */
	public String getAdministrador() {
		return administrador;
	}
	/**
	 * @param administrador the administrador to set
	 */
	public void setAdministrador(String administrador) {
		this.administrador = administrador;
	}

   


}
